// Power and Roots
console.log(Math.pow(2, 3)); // 8
console.log(Math.sqrt(25));  // 5
console.log(Math.cbrt(27));  // 3

// Minimun and Maximum
console.log(Math.min(3, 7, 1)); // 1
console.log(Math.max(3, 7, 1)); // 7

// Random Numbers
console.log(Math.random()); // between 0 and 1
// Example: random integer between 1 and 10
console.log(Math.floor(Math.random() * 10) + 1);

// Trigonometry
console.log(Math.sin(Math.PI / 2)); // 1
console.log(Math.cos(0));           // 1
console.log(Math.tan(Math.PI / 4)); // 1

// Logarithms & Exponentials
console.log(Math.log(1));       // 0 (natural log)
console.log(Math.log10(100));   // 2 (base 10 log)
console.log(Math.log2(8));      // 3 (base 2 log)
console.log(Math.exp(1));       // ~2.718 (e^1)